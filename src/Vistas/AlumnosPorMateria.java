    private void armarCabecera(){
        modelo.addColumn("ID");
        modelo.addColumn("DNI");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");
        jtTablaAlumnos.setModel(modelo);

        }
    private void vaciarTabla () {
        for (int i = modelo.getRowCount()-1; i >=0;i--){
            modelo.removeRow(i);
        }
        }
    public void agregarItems (){
        MateriaData matData = new MateriaData();
        List<Materia> listaMat = matData.listarMaterias();
        for (Materia aux :listaMat){
            jcMaterias.addItem(aux);
        }
    }
    private void cargarMateriasTabla () {
        InscripcionData insc =new InscripcionData();
        Materia materiaSelec = new  Materia();
        materiaSelec = (Materia) jcMaterias.getSelectedItem();
        List<Alumno> alumnos = insc.obternerAlumnoPorMateria(materiaSelec.getIdMateria());
        for (Alumno alumno : alumnos) {
            modelo.addRow(new Object[]{alumno.getIdAlumno(),alumno.getDni(),alumno.getApellido(),alumno.getNombre()});
        }
        
        
    }

